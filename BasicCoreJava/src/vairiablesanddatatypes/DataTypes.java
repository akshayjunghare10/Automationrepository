package vairiablesanddatatypes;

public class DataTypes {

	public static void main(String[] args) {

		// Integer value
int age=30;
System.out.println(age);

//Character value

char gender='M';
System.out.println(gender);

//Decimal value
float grade =8.7f;
System.out.println(grade);
double mrks=3.6;
System.out.println(mrks);

//Boolean value
boolean status=true;
System.out.println(status);

//Strings
String name="pooja";
System.out.println(name);

//For Integer values

byte a=10;
short b=20;
int c=30;
long d=40;


	}

}
